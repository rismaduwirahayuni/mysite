// Buat 4 function bernama tambah, kurang, kali, bagi
let number1 = 24;
let number2 = 2;

function tambah() {
    let result = number1 + number2;
    console.log(result)
}

function kurang() {
    let result = number1 - number2;
    console.log(result)
}

function bagi() {
    let result = number1 / number2;
    console.log(result)
}

function kali() {
    let result = number1 * number2;
    console.log(result)
}

tambah();
kurang();
bagi();
kali();